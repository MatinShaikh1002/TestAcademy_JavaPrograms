package October.Increment_Decrement_Operator;

public class Task1 {

    public static void main(String[] args) {

        int a=10;
        int b=++a;  // Pre increment condition b=11,
        System.out.println(a);
        System.out.println(b);

        // Output
        // Value of a=10
        // ++a--> increment first 11--> then store into b
        // b=11
        // Now value of a=11 and b=11

    }
}
